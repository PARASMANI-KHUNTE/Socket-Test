// models/User.js

const mongoose = require('mongoose');

// User Schema
const UserSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    mobile: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    ProfileUrl : {
        type : String
    }
});


const User = mongoose.model("User",UserSchema)

module.exports = User;
